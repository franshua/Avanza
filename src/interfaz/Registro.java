    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String user = jTextField1.getText();
        String pass = new String(jPasswordField1.getPassword());
        String pass2 = new String(jPasswordField2.getPassword());
        String mail = jTextField2.getText();
        if(pass.equals(pass2)){
            try{
                if(org.crearUsuario(user, pass , mail)){
                    Usuario login = org.login(mail, pass);
                    Principal p = new Principal();
                    p.setVisible(true);
                    this.setVisible(false);
                }
            }catch(ObjetoVacio | NoEncontrado e){
            }
        }else{
            JOptionPane.showMessageDialog(this, "El password no coincide", "Error", JOptionPane.WARNING_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

