    private void actualizaTabla(){
        try{
            Vector filas = new Vector();
            partidos = OrganizadorPartidos.getInstance().partidosInscritos();
            for(int c=0;c<partidos.size();c++){
                Vector fila = new Vector();
                fila.add(partidos.get(c).getDeporte().getNombre());
                fila.add(partidos.get(c).getLugar());
                fila.add(partidos.get(c).getFecha().getTime().toString());
                filas.add(fila);
            }
            DefaultTableModel tabla = new DefaultTableModel(filas, columnas){
                @Override
                public boolean isCellEditable(int fila, int columna){
                    return false;
                }
            };
            jTable1.setModel(tabla);
        } catch (CadenaVacia | FechaErronea | NumeroIncorrecto | ErrorConexionBd | ObjetoVacio ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
